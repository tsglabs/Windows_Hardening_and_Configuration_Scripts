# SECTION 7 - PERFORMANCE TUNING (GENETEC-SPECIFIC)
#
# These settings prevent the OS from interfering with Genetec's recording,
# decoding, and database operations. Each setting is explained in terms of
# its specific impact on SC performance.
# ════════════════════════════════════════════════════════════════════════════════
Write-TSGSection "PERFORMANCE TUNING -- GENETEC"

# Set High Performance power plan
#
# The High Performance plan prevents CPU P-state transitions that throttle
# processor speed. On Genetec Archiver servers, throttling causes dropped
# frames during peak recording load. On Security Desk workstations, it
# causes video decode stuttering at high camera counts.
#
# GUID 8c5e7fda-e8bf-4a96-9a85-a6e23a8c635c is the built-in High Performance
# plan present on all Windows versions. The script creates it if it has been
# removed by OEM imaging tools.
$highPerfGuid = "8c5e7fda-e8bf-4a96-9a85-a6e23a8c635c"
powercfg -setactive $highPerfGuid 2>$null
if ($LASTEXITCODE -ne 0) {
    # Plan was removed -- duplicate and activate it
    powercfg -duplicatescheme $highPerfGuid | Out-Null
    powercfg -setactive $highPerfGuid | Out-Null
}
Write-Host "    [OK] Power plan: High Performance" -ForegroundColor Green

# Disable hibernation and remove hiberfil.sys
#
# Recording servers must never hibernate. If the Archiver hibernates, it
# loses all active recording connections. Cameras will attempt to reconnect
# but the gap in recording cannot be recovered. hiberfil.sys also consumes
# significant disk space on systems with large RAM (16-64GB+) -- disk space
# that should be available for video archive storage.
powercfg -h off
Write-Host "    [OK] Hibernation disabled, hiberfil.sys removed" -ForegroundColor Green

# Disable Windows Search Indexing
#
# Genetec Security Center has its own media index for the video archive.
# The Windows Search Indexer (WSearch service) constantly reads files to
# build its index. On a system with a large Genetec archive, this creates
# competing I/O that degrades Archiver throughput. SC does not use Windows
# Search -- disabling it provides no functional loss.
Stop-Service -Name "WSearch" -ErrorAction SilentlyContinue
Set-Service  -Name "WSearch" -StartupType Disabled
Write-Host "    [OK] Windows Search Indexing disabled" -ForegroundColor Green

# Disable SysMain (Superfetch)
#
# SysMain pre-loads frequently used applications into RAM. On Genetec
# Archiver and Directory servers that run 24/7 with a fixed workload,
# SysMain provides no benefit and consumes RAM that should be available
# for the Archiver's video buffer and SQL Server's buffer pool.
Stop-Service -Name "SysMain" -ErrorAction SilentlyContinue
Set-Service  -Name "SysMain" -StartupType Disabled
Write-Host "    [OK] SysMain (Superfetch) disabled" -ForegroundColor Green

# Optimize visual effects for performance
#
# Security Desk operators do not need animated menus. Disabling visual effects
# frees GPU resources for video decoding. On systems with many simultaneous
# camera streams, this is a meaningful improvement.
Set-TSGRegistry `
    -Path "HKCU:\SOFTWARE\Microsoft\Windows\CurrentVersion\Explorer\VisualEffects" `
    -Name "VisualFXSetting" `
    -Value 2


