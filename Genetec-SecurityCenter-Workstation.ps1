# Write-TSGSection: prints a visible section header to the console and transcript
function Write-TSGSection {
    param([string]$Title, [string]$Subtitle = "")
    Write-Host ""
    Write-Host "  ======================================================================" -ForegroundColor DarkGray
    Write-Host "  $Title" -ForegroundColor Yellow
    if ($Subtitle) { Write-Host "  $Subtitle" -ForegroundColor DarkGray }
    Write-Host "  ======================================================================" -ForegroundColor DarkGray
}

# Set-TSGRegistry: idempotent registry write -- creates the key path if missing,
# sets the value, and logs OK or FAIL for every entry. Safe to re-run.
function Set-TSGRegistry {
    param(
        [Parameter(Mandatory)][string]$Path,
        [Parameter(Mandatory)][string]$Name,
        [Parameter(Mandatory)][object]$Value,
        [ValidateSet("DWord","QWord","String","ExpandString","Binary","MultiString")]
        [string]$Type = "DWord"
    )
    try {
        if (-not (Test-Path $Path)) { New-Item -Path $Path -Force -ErrorAction Stop | Out-Null }
        Set-ItemProperty -Path $Path -Name $Name -Value $Value -Type $Type -Force -ErrorAction Stop
        Write-Host "    [OK] $($Path.Split('\')[-1])\$Name = $Value" -ForegroundColor Green
    } catch {
        Write-Host "    [FAIL] $Name at $Path" -ForegroundColor Red
        Write-Host "           $($_.Exception.Message)" -ForegroundColor DarkGray
    }
}

# Remove-TSGAppx: removes an AppX package for all existing users and prevents
# reinstall for future accounts. Handles missing packages without errors.
function Remove-TSGAppx {
    param([Parameter(Mandatory)][string]$PackageName, [Parameter(Mandatory)][string]$FriendlyName)
    Write-Host "    Removing: $FriendlyName ($PackageName)" -ForegroundColor DarkGray
    try {
        Get-AppxProvisionedPackage -Online -ErrorAction SilentlyContinue |
            Where-Object { $_.DisplayName -like "*$PackageName*" } |
            Remove-AppxProvisionedPackage -Online -ErrorAction SilentlyContinue | Out-Null
        Get-AppxPackage -AllUsers -ErrorAction SilentlyContinue |
            Where-Object { $_.Name -like "*$PackageName*" } |
            Remove-AppxPackage -AllUsers -ErrorAction SilentlyContinue
        Write-Host "    [OK] $FriendlyName removed or already absent" -ForegroundColor Green
    } catch { Write-Host "    [SKIP] $FriendlyName -- $($_.Exception.Message)" -ForegroundColor DarkGray }
}

# Test-TSGEnterprise: checks the OS SKU and gates Enterprise-only controls.
# Credential Guard and AppLocker enforcement require Enterprise or Education.
# Returns $true on Enterprise/Server, $false on Pro with a logged warning.
# Source: learn.microsoft.com/en-us/windows/security/licensing-and-edition-requirements
function Test-TSGEnterprise {
    param([string]$ControlName = "This control")
    $sku = (Get-CimInstance Win32_OperatingSystem -ErrorAction Stop).OperatingSystemSKU
    # Enterprise=4, Enterprise N=27, Enterprise Eval=70, Enterprise N Eval=84,
    # Enterprise G=125, Education=4, Server variants=161-172
    if ($sku -in @(4,27,70,84,125,161,162,163,164,169,170,171,172)) { return $true }
    Write-Host "    [SKIP] $ControlName requires Enterprise or Education license (SKU: $sku)" -ForegroundColor Yellow
    Write-Host "           Source: learn.microsoft.com/windows/security/licensing-and-edition-requirements" -ForegroundColor DarkGray
    $script:SkippedEntControls.Add($ControlName)
    return $false
}

# New-TSGRestorePoint: creates a System Restore point before hardening.
# Can fail on domain machines with Group Policy restrictions -- not fatal.
function New-TSGRestorePoint {
    param([string]$Description = "Hardening Script")
    Write-Host "  Creating system restore point..." -ForegroundColor DarkGray
    try {
        Checkpoint-Computer -Description "$Description $(Get-Date -Format 'yyyy-MM-dd HH:mm')" `
            -RestorePointType "MODIFY_SETTINGS" -ErrorAction Stop
        Write-Host "  [OK] Restore point created" -ForegroundColor Green
    } catch {
        Write-Host "  [WARN] Restore point failed -- continuing ($($_.Exception.Message))" -ForegroundColor Yellow
    }
}

# Disable-TSGNetBIOS: disables NetBIOS over TCP/IP on all IPv4 adapters.
# Uses WMI because there is no native PowerShell cmdlet for this.
# SetTcpipNetbios(2) = disable. Value 1 = enable. Value 0 = use DHCP setting.
function Disable-TSGNetBIOS {
    Write-Host "    Disabling NetBIOS over TCP/IP on all adapters..." -ForegroundColor DarkGray
    try {
        $adapters = Get-WmiObject -Class Win32_NetworkAdapterConfiguration `
            -Filter "IPEnabled = True" -ErrorAction Stop
        $count = 0
        foreach ($adapter in $adapters) {
            $result = $adapter.SetTcpipNetbios(2)
            if ($result.ReturnValue -eq 0) { $count++ }
        }
        Write-Host "    [OK] NetBIOS disabled on $count adapter(s)" -ForegroundColor Green
    } catch { Write-Host "    [FAIL] NetBIOS disable -- $($_.Exception.Message)" -ForegroundColor Red }
}

