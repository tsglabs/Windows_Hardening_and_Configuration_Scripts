# ════════════════════════════════════════════════════════════════════════════════
# SECTION 1 - CREDENTIAL AND IDENTITY PROTECTION
#
# Genetec Directory service accounts and SQL credentials are high-value targets.
# Credential dumping tools like Mimikatz are the most common way attackers
# escalate after initial access on a VMS workstation.
# ════════════════════════════════════════════════════════════════════════════════
Write-TSGSection "CREDENTIAL AND IDENTITY PROTECTION"

# Disable WDigest credential caching in LSASS
# Source: DISA STIG WN11-CC-000040 | CIS 18.9.29.1 | CCCS ITSP.70.012
#
# WDigest stores credentials in LSASS memory as cleartext. An attacker with
# admin access can dump these using Mimikatz or similar tools. Setting
# UseLogonCredential = 0 prevents this. Works on all editions.
Set-TSGRegistry `
    -Path "HKLM:\SYSTEM\CurrentControlSet\Control\SecurityProviders\WDigest" `
    -Name "UseLogonCredential" `
    -Value 0

# Do not store the LAN Manager hash
# Source: DISA STIG WN11-SO-000190 | CIS 2.3.11.6 | CCCS ITSP.70.012
#
# LM hashes are computed using a trivially weak algorithm and can be cracked
# offline in seconds with modern hardware. Setting NoLMHash = 1 prevents the
# LM hash from being stored alongside the stronger NTLM hash.
Set-TSGRegistry `
    -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Lsa" `
    -Name "NoLMHash" `
    -Value 1

# Require NTLMv2, refuse LM and NTLMv1
# Source: DISA STIG WN11-SO-000195 | CIS 2.3.11.7 | CCCS ITSP.70.012
#
# LmCompatibilityLevel = 5: send NTLMv2 response only, refuse all LM and
# NTLMv1 authentication. NTLMv1 is crackable in seconds offline.
# Genetec uses Kerberos on domain and NTLM fallback on workgroup -- NTLMv2 works.
Set-TSGRegistry `
    -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Lsa" `
    -Name "LmCompatibilityLevel" `
    -Value 5

# Enable LSA Protection (RunAsPPL)
# Source: DISA STIG WN11-SO-000100 | CIS 18.3.2 | CCCS ITSP.70.012
#
# Runs LSASS as a Protected Process Light. Blocks most credential-dumping
# tools including Mimikatz in its default configuration. Works on Pro and
# Enterprise. Restart required for this change to take effect.
Set-TSGRegistry `
    -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Lsa" `
    -Name "RunAsPPL" `
    -Value 1
Write-Host "    [NOTE] Restart required for LSA Protection to activate" -ForegroundColor Yellow

# Credential Guard (Enterprise/Education only)
# Source: DISA STIG WN11-00-000095 | NSA/CISA | CCCS ITSP.70.012
#
# Isolates LSASS in a Hyper-V VBS container. Even with admin access, an
# attacker cannot dump credentials from the isolated process. Requires
# UEFI + Secure Boot + TPM 2.0. Skipped automatically on Pro.
if (Test-TSGEnterprise -ControlName "Credential Guard") {
    Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\DeviceGuard" -Name "EnableVirtualizationBasedSecurity" -Value 1
    Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\DeviceGuard" -Name "RequirePlatformSecurityFeatures" -Value 3
    Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\DeviceGuard\Scenarios\CredentialGuard" -Name "Enabled" -Value 1
    Write-Host "    [NOTE] Restart required for Credential Guard to activate" -ForegroundColor Yellow
}


# ════════════════════════════════════════════════════════════════════════════════
# SECTION 2 - NETWORK HARDENING
#
# Genetec communicates over TCP using well-defined ports. It does not rely on
# NetBIOS or LLMNR for any discovery function. Disabling them removes attack
# surface without affecting SC functionality.
# ════════════════════════════════════════════════════════════════════════════════
Write-TSGSection "NETWORK HARDENING"

# Disable SMBv1
# Source: DISA STIG WN11-00-000160/165 | CIS 18.3.3 | NSA | CCCS ITSP.70.012
#
# SMBv1 is the protocol exploited by EternalBlue (WannaCry, NotPetya, BadRabbit).
# No Genetec component requires SMBv1. Disable unconditionally.
# The feature removal and the registry key are belt-and-suspenders -- both needed.
Set-TSGRegistry `
    -Path "HKLM:\SYSTEM\CurrentControlSet\Services\LanmanServer\Parameters" `
    -Name "SMB1" `
    -Value 0
Disable-WindowsOptionalFeature -Online -FeatureName "SMB1Protocol" -NoRestart -ErrorAction SilentlyContinue | Out-Null
Write-Host "    [OK] SMBv1 feature removed and registry key set" -ForegroundColor Green

# Remove PowerShell v2
# Source: DISA STIG WN11-00-000155 | NSA/CISA June 2022 CSI | CCCS ITSP.70.012
#
# PowerShell v2 bypasses all modern security features: AMSI, script block
# logging, constrained language mode. NSA/CISA specifically call out PS v2
# removal in their June 2022 PowerShell security guidance.
# Genetec installer and management scripts use PS 5+.
Disable-WindowsOptionalFeature -Online -FeatureName "MicrosoftWindowsPowerShellV2Root" -NoRestart -ErrorAction SilentlyContinue | Out-Null
Write-Host "    [OK] PowerShell v2 engine removed" -ForegroundColor Green

# Disable LLMNR
# Source: DISA STIG WN11-CC-000085 | CIS 18.5.4 | NSA/CISA | CCCS ITSP.70.012
#
# LLMNR broadcasts unauthenticated name resolution requests to the local subnet.
# Responder and Inveigh capture NTLMv2 hashes from LLMNR queries passively.
# Genetec uses DNS for all name resolution -- LLMNR is pure attack surface.
Set-TSGRegistry `
    -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows NT\DNSClient" `
    -Name "EnableMulticast" `
    -Value 0

# Disable NetBIOS over TCP/IP
# Source: DISA STIG WN11-CC-000080 | NSA | CCCS ITSP.70.012
#
# NetBIOS exposes machine name, domain, and OS version to the local subnet
# and is exploited for lateral movement. Genetec uses DNS and TCP directly.
Disable-TSGNetBIOS

# Require SMB packet signing
# Source: DISA STIG WN11-SO-000160/165 | CIS 2.3.9.1/2.3.9.2 | CCCS ITSP.70.012
#
# SMB signing ensures packets have not been tampered with in transit.
# Prevents SMB relay attacks where an attacker intercepts authentication
# and uses it to access Genetec archive shares. Both keys are required.
Set-TSGRegistry `
    -Path "HKLM:\SYSTEM\CurrentControlSet\Services\LanmanServer\Parameters" `
    -Name "RequireSecuritySignature" `
    -Value 1
Set-TSGRegistry `
    -Path "HKLM:\SYSTEM\CurrentControlSet\Services\LanmanWorkstation\Parameters" `
    -Name "RequireSecuritySignature" `
    -Value 1

# Enable Windows Firewall on all profiles
# Source: DISA STIG WN11-NX-000010/015/020 | CIS 9.1.1/9.2.1/9.3.1 | CCCS
#
# The host-based firewall is the last line of defense when network controls
# fail. Enable on all profiles. Note: no spaces in the profile list --
# "Domain,Public,Private" is the correct syntax for Set-NetFirewallProfile.
Set-NetFirewallProfile -Profile Domain,Public,Private -Enabled True
Set-NetFirewallProfile -Profile Domain,Public,Private -DefaultInboundAction Block
Write-Host "    [OK] Firewall enabled with default-deny inbound on all profiles" -ForegroundColor Green

# Open required Genetec Security Center firewall ports
# Source: Genetec Security Center Hardening Guide (publications.genetec.com)
#
# These are the default SC communication ports. Adjust if your deployment
# uses non-default port assignments configured in Config Tool.
#
# TCP 5500  - Genetec Directory SDK (client connections)
# TCP 443   - Genetec Web Client and REST API (HTTPS)
# TCP 554   - Genetec Archiver RTSP (live and playback streams)
# TCP 555   - Genetec Archiver (proprietary stream protocol)
# TCP 8888  - Genetec Unit Assistant (camera unit management)
# TCP 4502  - Genetec Patroller (LPR, if used)
Write-Host "    Adding Genetec firewall rules..." -ForegroundColor DarkGray

$genetecRules = @(
    @{ Name = "Genetec-Directory-SDK";   Port = 5500; Description = "Genetec Directory SDK client connections" },
    @{ Name = "Genetec-HTTPS";           Port = 443;  Description = "Genetec Web Client and REST API" },
    @{ Name = "Genetec-Archiver-RTSP";   Port = 554;  Description = "Genetec Archiver RTSP streams" },
    @{ Name = "Genetec-Archiver-Stream"; Port = 555;  Description = "Genetec Archiver proprietary stream protocol" },
    @{ Name = "Genetec-Unit-Assistant";  Port = 8888; Description = "Genetec Unit Assistant camera management" }
)

foreach ($rule in $genetecRules) {
    # Remove existing rule first to avoid duplicates on re-run (idempotent)
    Remove-NetFirewallRule -DisplayName $rule.Name -ErrorAction SilentlyContinue
    New-NetFirewallRule `
        -DisplayName $rule.Name `
        -Direction Inbound `
        -Protocol TCP `
        -LocalPort $rule.Port `
        -Action Allow `
        -Description $rule.Description `
        -ErrorAction SilentlyContinue | Out-Null
    Write-Host "    [OK] Firewall rule: $($rule.Name) (TCP $($rule.Port))" -ForegroundColor Green
}


# ════════════════════════════════════════════════════════════════════════════════
# SECTION 3 - WINDOWS DEFENDER (GENETEC-TUNED)
#
# Windows Defender is compatible with Genetec Security Center. The key is
# proper exclusions. Without them, Defender will scan video archive files
# on every read, causing severe I/O contention on the Archiver.
# ════════════════════════════════════════════════════════════════════════════════
Write-TSGSection "WINDOWS DEFENDER -- GENETEC CONFIGURATION"

# Ensure Defender is running
# Source: DISA STIG WN11-00-000275
Set-Service -Name "WinDefend" -StartupType Automatic -ErrorAction SilentlyContinue
Start-Service -Name "WinDefend" -ErrorAction SilentlyContinue
Write-Host "    [OK] Windows Defender service running" -ForegroundColor Green

# Enable real-time protection
# Source: DISA STIG WN11-00-000270
Set-MpPreference -DisableRealtimeMonitoring $false

# Enable network protection (blocks known malicious IPs/domains at kernel level)
Set-MpPreference -EnableNetworkProtection Enabled

# Disable cloud-delivered protection
#
# Most Genetec deployments run on isolated camera networks with restricted
# outbound internet access. Cloud protection makes outbound connections to
# Microsoft for real-time threat intelligence. Disable it to avoid connectivity
# errors and unexpected latency on air-gapped or restricted networks.
# Remove this block if your network permits outbound HTTPS.
Set-MpPreference -MAPSReporting Disabled
Set-MpPreference -SubmitSamplesConsent NeverSend
Write-Host "    [OK] Cloud protection disabled (isolated network assumption)" -ForegroundColor Green
Write-Host "    [NOTE] Re-enable cloud protection if outbound internet is permitted" -ForegroundColor Yellow

# Do NOT enable Controlled Folder Access on Genetec workstations
#
# Genetec Security Center writes continuously to AppData paths, ProgramData,
# and custom archive directories during normal operation. CFA will block
# these writes until every write path is explicitly whitelisted.
# The result is recording gaps and service failures during initial deployment.
# If you want CFA, run Set-MpPreference -EnableControlledFolderAccess AuditMode
# first, review the event log for 30 days, add all legitimate paths, then
# switch to Enabled.
Write-Host "    [NOTE] Controlled Folder Access intentionally not enabled" -ForegroundColor Yellow
Write-Host "           Use AuditMode first to build the Genetec exclusion list" -ForegroundColor Yellow

# Add Defender exclusions for Genetec-specific file types and paths
#
# These exclusions prevent Defender from scanning video archive content,
# which generates massive I/O with no security benefit. Video files are
# not executable -- scanning them does not catch malware. The tradeoff
# (reduced I/O vs. theoretical scan coverage) is always correct for VMS.

Write-Host "    Adding Genetec-specific Defender exclusions..." -ForegroundColor DarkGray

# Genetec installation directories
# Prevents Defender from scanning SC binaries on every access
$genetecPaths = @(
    "C:\Program Files (x86)\Genetec Security Center",
    "C:\Program Files\Genetec Security Center"
)
foreach ($path in $genetecPaths) {
    Add-MpPreference -ExclusionPath $path -ErrorAction SilentlyContinue
    Write-Host "    [OK] Exclusion path: $path" -ForegroundColor Green
}

# Video archive file extensions
# G64 and G64x are Genetec's proprietary video container formats.
# Scanning them on read causes severe I/O contention on the Archiver,
# especially on systems with large archives (multi-TB).
$genetecExtensions = @("g64", "g64x")
foreach ($ext in $genetecExtensions) {
    Add-MpPreference -ExclusionExtension $ext -ErrorAction SilentlyContinue
    Write-Host "    [OK] Exclusion extension: .$ext" -ForegroundColor Green
}

# SQL database files
# Genetec Directory uses SQL Server (Express or full) for its database.
# Defender scanning open SQL data files causes errors and performance
# degradation. Microsoft recommends excluding these for all SQL deployments.
# Source: support.microsoft.com/en-us/topic/how-to-choose-antivirus-software-to-run-on-computers-that-are-running-sql-server-feda079b-3e24-186b-945a-3051f6f3a584
$sqlExtensions = @("mdf", "ldf", "ndf")
foreach ($ext in $sqlExtensions) {
    Add-MpPreference -ExclusionExtension $ext -ErrorAction SilentlyContinue
    Write-Host "    [OK] Exclusion extension: .$ext (SQL data file)" -ForegroundColor Green
}


# ════════════════════════════════════════════════════════════════════════════════
# SECTION 4 - AUDIT POLICY AND LOGGING
#
# Genetec workstations generate significant event log volume. Increasing log
# sizes is essential -- the default 64MB Security log fills in hours on an
# active Archiver or Directory server.
# ════════════════════════════════════════════════════════════════════════════════
Write-TSGSection "AUDIT POLICY AND LOGGING"

# Logon and logoff events
# Source: DISA STIG WN11-AU-000030/035 | CIS 17.5.1 | CCCS ITSP.70.012
#
# Foundation of any incident investigation. Without these, you cannot
# reconstruct who accessed the Genetec server or when.
AuditPol /set /subcategory:"Logon" /success:enable /failure:enable | Out-Null
AuditPol /set /subcategory:"Logoff" /success:enable | Out-Null
AuditPol /set /subcategory:"Account Lockout" /failure:enable | Out-Null
Write-Host "    [OK] Logon/logoff/lockout auditing enabled" -ForegroundColor Green

# Process creation with command-line arguments
# Source: DISA STIG WN11-AU-000500/505 | NSA/CISA | CCCS ITSP.70.012
#
# Logs every process started including the full command line (Event 4688).
# Critical for detecting malware or unauthorized tools running on a
# Genetec server. Both the auditpol command and the registry key are
# required -- the registry key enables the command-line capture in 4688.
AuditPol /set /subcategory:"Process Creation" /success:enable | Out-Null
Set-TSGRegistry `
    -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System\Audit" `
    -Name "ProcessCreationIncludeCmdLine_Enabled" `
    -Value 1

# Audit policy changes
# Source: DISA STIG WN11-AU-000525/530 | CIS 17.9.1
#
# Detects if the audit trail itself is tampered with. If an attacker
# modifies audit policy to stop logging, that change is captured here.
AuditPol /set /subcategory:"Audit Policy Change" /success:enable /failure:enable | Out-Null
Write-Host "    [OK] Audit policy configured" -ForegroundColor Green

# Increase event log sizes
# Source: DISA STIG WN11-AU-000045 | CIS 18.9.26.1 | CCCS ITSP.70.012
#
# Genetec Archiver and Directory generate significant event volume.
# The default 64MB Security log fills in hours on an active system.
# Setting it to 1GB provides approximately 30 days of retention on
# a typical Genetec deployment.
wevtutil sl Security    /ms:1073741824 | Out-Null   # 1 GB
wevtutil sl Application /ms:268435456  | Out-Null   # 256 MB (Genetec logs here)
wevtutil sl System      /ms:268435456  | Out-Null   # 256 MB
Write-Host "    [OK] Event log sizes: Security 1GB, Application 256MB, System 256MB" -ForegroundColor Green

# PowerShell Script Block Logging
# Source: DISA STIG WN11-CC-000315 | NSA/CISA June 2022 CSI | CCCS ITSP.70.012
#
# Logs the full content of every PS script block as it executes,
# including deobfuscated code. Captures attacker activity even when
# scripts are obfuscated. Recommended by NSA/CISA as a priority control.
Set-TSGRegistry `
    -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ScriptBlockLogging" `
    -Name "EnableScriptBlockLogging" `
    -Value 1
Set-TSGRegistry `
    -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ScriptBlockLogging" `
    -Name "EnableScriptBlockInvocationLogging" `
    -Value 1
# Increase the PS Operational log to handle the volume of script block events
wevtutil sl "Microsoft-Windows-PowerShell/Operational" /ms:104857600 | Out-Null  # 100 MB
Write-Host "    [OK] PowerShell Script Block Logging enabled (PS log: 100MB)" -ForegroundColor Green


# ════════════════════════════════════════════════════════════════════════════════
# SECTION 5 - ACCESS CONTROL
#
# UAC and autorun controls. RDP is left enabled with NLA enforced because
# Genetec administrators need remote access to Config Tool and Security Desk.
# ════════════════════════════════════════════════════════════════════════════════
Write-TSGSection "ACCESS CONTROL"

# Enable UAC
# Source: DISA STIG WN11-SO-000245 | CIS 2.3.17.1 | CCCS ITSP.70.012
#
# User Account Control should never be disabled. EnableLUA = 1 is the
# registry enforcement.
#
# NOTE: ConsentPromptBehaviorAdmin is set to 1 (notify for app changes, not
# Windows-signed binaries) rather than 2 (always notify). Genetec Config Tool
# occasionally prompts for elevation. Max-level UAC causes double prompts
# that confuse operators. Standard prompt is the right tradeoff here.
Set-TSGRegistry `
    -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System" `
    -Name "EnableLUA" `
    -Value 1
