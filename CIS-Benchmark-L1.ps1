Set-StrictMode -Version Latest
$ErrorActionPreference = "Continue"

$LogDate = Get-Date -Format "yyyyMMdd"
$LogPath = "C:\Logs\CIS-Benchmark-L1_$LogDate.log"
if (-not (Test-Path "C:\Logs")) { New-Item -ItemType Directory -Path "C:\Logs" -Force | Out-Null }
Start-Transcript -Path $LogPath -Append -IncludeInvocationHeader | Out-Null

$sharedModule = Join-Path $PSScriptRoot "TSG-Shared.psm1"
if (Test-Path $sharedModule) { . $sharedModule }
else {
    function Write-TSGSection { param([string]$Title, [string]$Subtitle = "")
        Write-Host ""; Write-Host "  == $Title" -ForegroundColor Yellow
        if ($Subtitle) { Write-Host "     $Subtitle" -ForegroundColor DarkGray }
    }
    function Set-TSGRegistry { param([string]$Path, [string]$Name, [object]$Value, [string]$Type = "DWord")
        try {
            if (-not (Test-Path $Path)) { New-Item -Path $Path -Force | Out-Null }
            Set-ItemProperty -Path $Path -Name $Name -Value $Value -Type $Type -Force -ErrorAction Stop
            Write-Host "    [OK] $($Path.Split('\')[-1])\$Name = $Value" -ForegroundColor Green
        } catch { Write-Host "    [FAIL] $Name -- $($_.Exception.Message)" -ForegroundColor Red }
    }
    function Remove-TSGAppx { param([string]$PackageName, [string]$FriendlyName)
        try {
            Get-AppxProvisionedPackage -Online -ErrorAction SilentlyContinue | Where-Object { $_.DisplayName -like "*$PackageName*" } | Remove-AppxProvisionedPackage -Online -ErrorAction SilentlyContinue | Out-Null
            Get-AppxPackage -AllUsers -ErrorAction SilentlyContinue | Where-Object { $_.Name -like "*$PackageName*" } | Remove-AppxPackage -AllUsers -ErrorAction SilentlyContinue
            Write-Host "    [OK] $FriendlyName removed or already absent" -ForegroundColor Green
        } catch { Write-Host "    [SKIP] $FriendlyName" -ForegroundColor DarkGray }
    }
    function New-TSGRestorePoint { param([string]$Description = "Hardening")
        try { Checkpoint-Computer -Description "$Description $(Get-Date -Format 'yyyy-MM-dd')" -RestorePointType "MODIFY_SETTINGS" -ErrorAction Stop; Write-Host "  [OK] Restore point created" -ForegroundColor Green }
        catch { Write-Host "  [WARN] Restore point failed -- continuing" -ForegroundColor Yellow }
    }
}

Write-Host ""; Write-Host "  =========================================================" -ForegroundColor Cyan
Write-Host "  CIS Benchmark Level 1" -ForegroundColor Cyan
Write-Host "  CIS Windows 11/10 Benchmark v3.0 -- Level 1 controls" -ForegroundColor White
Write-Host "  Hans Study, CISSP | https://hans.study" -ForegroundColor DarkGray
Write-Host "  Log: $LogPath" -ForegroundColor DarkGray
Write-Host "  =========================================================" -ForegroundColor Cyan; Write-Host ""

New-TSGRestorePoint -Description "CIS Benchmark L1 Baseline"

Write-TSGSection "CREDENTIAL AND IDENTITY" "CIS 18.9.29.1 | CIS 2.3.11.6/7 | CIS 18.3.2"
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\SecurityProviders\WDigest" -Name "UseLogonCredential" -Value 0   # CIS 18.9.29.1
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Lsa" -Name "NoLMHash"           -Value 1                          # CIS 2.3.11.6
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Lsa" -Name "LmCompatibilityLevel" -Value 5                        # CIS 2.3.11.7
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Lsa" -Name "RunAsPPL"           -Value 1                          # CIS 18.3.2
Write-Host "    [NOTE] Restart required for LSA Protection -- CIS 18.3.2" -ForegroundColor Yellow
Write-Host "    [NOTE] Credential Guard is CIS Level 2. Not included here." -ForegroundColor DarkGray

Write-TSGSection "NETWORK HARDENING" "CIS 18.3.3 | CIS 18.5.4 | CIS 9.x | CIS 2.3.9.1/2"
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Services\LanmanServer\Parameters" -Name "SMB1" -Value 0               # CIS 18.3.3
Disable-WindowsOptionalFeature -Online -FeatureName "SMB1Protocol" -NoRestart -ErrorAction SilentlyContinue | Out-Null
Disable-WindowsOptionalFeature -Online -FeatureName "MicrosoftWindowsPowerShellV2Root" -NoRestart -ErrorAction SilentlyContinue | Out-Null
Write-Host "    [OK] SMBv1 and PS v2 removed" -ForegroundColor Green
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows NT\DNSClient" -Name "EnableMulticast" -Value 0              # CIS 18.5.4
Set-NetFirewallProfile -Profile Domain,Public,Private -Enabled True                                                           # CIS 9.1.1/9.2.1/9.3.1
Set-NetFirewallProfile -Profile Domain,Public,Private -DefaultInboundAction Block                                             # CIS 9.1.2/9.2.2/9.3.2
Write-Host "    [OK] Firewall enabled, default-deny inbound" -ForegroundColor Green
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Services\LanmanServer\Parameters"    -Name "RequireSecuritySignature" -Value 1  # CIS 2.3.9.1
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Services\LanmanWorkstation\Parameters" -Name "RequireSecuritySignature" -Value 1  # CIS 2.3.9.2

Write-TSGSection "WINDOWS DEFENDER" "DISA WN11-00-000270/275 | CIS recommended"
Set-Service -Name "WinDefend" -StartupType Automatic -ErrorAction SilentlyContinue
Start-Service -Name "WinDefend" -ErrorAction SilentlyContinue
Set-MpPreference -DisableRealtimeMonitoring $false
Set-MpPreference -EnableNetworkProtection Enabled
Write-Host "    [OK] Defender configured" -ForegroundColor Green

Write-TSGSection "AUDIT POLICY AND LOGGING" "CIS 17.1.1 | CIS 17.5.1 | CIS 17.7.1 | CIS 17.8.1 | CIS 17.9.1 | CIS 18.9.26.1"
AuditPol /set /subcategory:"Logon"                   /success:enable /failure:enable | Out-Null  # CIS 17.5.1
AuditPol /set /subcategory:"Logoff"                  /success:enable                 | Out-Null
AuditPol /set /subcategory:"Account Lockout"         /failure:enable                 | Out-Null
AuditPol /set /subcategory:"Credential Validation"   /success:enable /failure:enable | Out-Null  # CIS 17.1.1
AuditPol /set /subcategory:"Process Creation"        /success:enable                 | Out-Null  # CIS 17.8.1
AuditPol /set /subcategory:"Sensitive Privilege Use" /success:enable /failure:enable | Out-Null  # CIS 17.7.1
AuditPol /set /subcategory:"Audit Policy Change"     /success:enable /failure:enable | Out-Null  # CIS 17.9.1
Write-Host "    [OK] Audit policy configured" -ForegroundColor Green
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System\Audit" -Name "ProcessCreationIncludeCmdLine_Enabled" -Value 1
wevtutil sl Security    /ms:1073741824 | Out-Null   # CIS 18.9.26.1 -- minimum 1 GB
wevtutil sl Application /ms:268435456  | Out-Null
wevtutil sl System      /ms:268435456  | Out-Null
Write-Host "    [OK] Log sizes: Security 1GB" -ForegroundColor Green
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ScriptBlockLogging" -Name "EnableScriptBlockLogging"         -Value 1
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ScriptBlockLogging" -Name "EnableScriptBlockInvocationLogging" -Value 1
wevtutil sl "Microsoft-Windows-PowerShell/Operational" /ms:104857600 | Out-Null
Write-Host "    [OK] PS Script Block Logging enabled" -ForegroundColor Green

Write-TSGSection "ACCESS CONTROL" "CIS 2.3.17.1 | CIS 18.9.8.1 | CIS 18.9.59.2.1"
# CIS L1 sets ConsentPromptBehaviorAdmin = 1 (notify for app changes, not Windows binaries)
# CIS L2 requires Always Notify (= 2). This is Level 1.
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System" -Name "EnableLUA"                 -Value 1  # CIS 2.3.17.1
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System" -Name "PromptOnSecureDesktop"      -Value 1
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System" -Name "ConsentPromptBehaviorAdmin" -Value 1  # CIS L1 = 1, L2 = 2
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\Explorer" -Name "NoDriveTypeAutoRun" -Value 255     # CIS 18.9.8.1
Set-TSGRegistry -Path "HKCU:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\Explorer" -Name "NoDriveTypeAutoRun" -Value 255
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Terminal Server\WinStations\RDP-Tcp" -Name "UserAuthentication" -Value 1  # CIS 18.9.59.2.1
Set-ExecutionPolicy RemoteSigned -Scope LocalMachine -Force
$officeVersion = "16.0"
foreach ($app in @("Word", "Excel", "PowerPoint", "Outlook", "Access")) {
    Set-TSGRegistry -Path "HKCU:\SOFTWARE\Policies\Microsoft\Office\$officeVersion\$app\Security" -Name "VBAWarnings" -Value 4
}
Write-Host "    [OK] Access control applied" -ForegroundColor Green

Write-TSGSection "TELEMETRY AND PRIVACY" "CIS 18.9.16.1 | CIS 18.9.44.1 | CIS 18.9.46.1"
Stop-Service -Name "DiagTrack" -ErrorAction SilentlyContinue
Set-Service  -Name "DiagTrack" -StartupType Disabled
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\DataCollection" -Name "AllowTelemetry" -Value 0              # CIS 18.9.16.1
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\DataCollection" -Name "AllowTelemetry" -Value 0
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\System" -Name "PublishUserActivities" -Value 0               # CIS 18.9.46.1
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\System" -Name "UploadUserActivities"  -Value 0
Set-TSGRegistry -Path "HKLM:\SOFTWARE\Policies\Microsoft\Windows\WindowsAI" -Name "DisableAIDataAnalysis" -Value 1
Set-TSGRegistry -Path "HKCU:\SOFTWARE\Policies\Microsoft\Windows\WindowsAI" -Name "DisableAIDataAnalysis" -Value 1
Disable-WindowsOptionalFeature -Online -FeatureName "Recall" -NoRestart -ErrorAction SilentlyContinue | Out-Null
Set-TSGRegistry -Path "HKCU:\SOFTWARE\Microsoft\Windows\CurrentVersion\AdvertisingInfo" -Name "Enabled" -Value 0              # CIS 18.9.44.1
Write-Host "    [OK] Telemetry, Recall, Advertising ID disabled" -ForegroundColor Green

Write-TSGSection "EXPLOIT MITIGATION" "CIS 18.8.21.5 | CIS | NSA | CCCS ITSP.70.012"
bcdedit /set nx AlwaysOn | Out-Null                                                                                # CIS 18.8.21.5
Write-Host "    [OK] DEP AlwaysOn (restart required)" -ForegroundColor Green
Set-ProcessMitigation -System -Enable ASLR,ForceRelocateImages -ErrorAction SilentlyContinue
Write-Host "    [OK] ASLR and forced relocation enabled" -ForegroundColor Green
Set-TSGRegistry -Path "HKLM:\SYSTEM\CurrentControlSet\Control\Session Manager\kernel" -Name "DisableExceptionChainValidation" -Value 0  # Value 0 = SEHOP enabled

Write-TSGSection "FINALIZATION"
Write-Host ""; Write-Host "  CIS Benchmark Level 1 applied." -ForegroundColor Cyan
Write-Host "  Log: $LogPath" -ForegroundColor White
Write-Host "  RESTART REQUIRED: DEP, LSA Protection" -ForegroundColor Yellow
Write-Host "  Hans Study | https://hans.study | contact@hans.study" -ForegroundColor DarkGray

$restart = Read-Host "  Restart now? [y/N]"
if ($restart -match '^[Yy]') { Write-Host "  Restarting..." -ForegroundColor Yellow; Start-Sleep -Seconds 15; Restart-Computer -Force }
Stop-Transcript | Out-Null
